# دالة لاستخراج اسم المستخدم باستخدام selenium
def get_username(url):
    driver.get(url)
    # العثور على عنصر اسم المستخدم بناءً على التنسيق في الصفحة
    user_element = driver.find_element("xpath", "//h1[contains(@class, 'share-title')]")
    return user_element.text.strip() if user_element else None

